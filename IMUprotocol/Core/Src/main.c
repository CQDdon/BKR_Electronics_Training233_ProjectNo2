#define DATA_LENGTH 33
#define START_BYTE 0x55

uint8_t dataRx;
uint8_t dataBuffer[DATA_LENGTH];
uint8_t index = 0;
extern float axisAcc[3];
extern float axisAngVel[3];
extern float axisAng[3];

volatile uint8_t uartReadyToSend = 0;
char uartDataToSend[256];

void sendUART(const char* data) {
    strncpy(uartDataToSend, data, sizeof(uartDataToSend) - 1);
    uartDataToSend[sizeof(uartDataToSend) - 1] = '\0';
    uartReadyToSend = 1;
}

void sendCommandSequence() {
    uint8_t unlock[] = {0xFF, 0xAA, 0x69, 0x88, 0xB5};
    uint8_t command[] = {0xFF, 0xAA, 0x01, 0x04, 0x00};
    uint8_t save[] = {0xFF, 0xAA, 0x00, 0x00, 0x00};

    // Step 1: Unlock
    HAL_UART_Transmit(&huart1, unlock, sizeof(unlock), 100);

    // Step 2: Send the command
    HAL_UART_Transmit(&huart1, command, sizeof(command), 100);

    // Step 3: Save
    HAL_UART_Transmit(&huart1, save, sizeof(save), 100);
}

// Nhan 33 byte, gom 11x3 byte cua gia toc, van toc goc va goc
void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
    // Chi nhan tin hieu tu cong UART1
    if (huart == &huart1)
    {
        // Kiem tra byte dau tien
        if (index == 0 && dataRx != START_BYTE)
        {
            // Neu byte dau tien khong phai la 0x55, nhan lai tu dau
            HAL_UART_Receive_IT(&huart1, &dataRx, 1);
            return;
        }
        // Luu du lieu vào bo dem
        dataBuffer[index++] = dataRx;
        // Neu nhan du 33 byte
        if (index == DATA_LENGTH)
        {
            // Thuc hien xu ly du lieu o day
						dataHandle(dataBuffer);
            // Ðat lai index de bat dau nhan du lieu moi
            index = 0;
        }
        // Tiep tuc nhan byte du lieu moi
        HAL_UART_Receive_IT(&huart1, &dataRx, 1);
    } else if (huart == &huart2) 
		{
			//nhan lenh R thi gui lenh reset
				if (dataRx == 'R') {
            sendCommandSequence();
        }
        HAL_UART_Receive_IT(&huart2, &dataRx, 1); // Nhan byte tiep theo
    }
}

// Moi khi co update interrupt thi se gui chuoi bao gom 8 thong so
void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim) {
    if (htim->Instance == TIM1) {
        if (uartReadyToSend) {
            HAL_UART_Transmit_IT(&huart2, (uint8_t*)uartDataToSend, strlen(uartDataToSend));
            uartReadyToSend = 0;
        } else {
            char output[256];
            snprintf(output, sizeof(output),
                "Acceleration X: %.4f\nAcceleration Y: %.4f\nAcceleration Z: %.4f\n"
                "Angular velocity X: %.4f\nAngular velocity Y: %.4f\nAngular velocity Z: %.4f\n"
                "Angle X: %.4f\nAngle Y: %.4f\nAngle Z: %.4f\n",
                axisAcc[0], axisAcc[1], axisAcc[2],
                axisAngVel[0], axisAngVel[1], axisAngVel[2],
                axisAng[0], axisAng[1], axisAng[2]);
            sendUART(output);
        }
    }
}


